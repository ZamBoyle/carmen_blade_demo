    <h1 class="text-center">Nos objectifs :</h1>
    <h2>1) Handisport</h2>

    <p>Proposer en province de Liège un club de Cécifoot B1 qui permettra aux joueurs déficients visuels de bénéficier
        d’infrastructures de qualité afin que ceux-ci puissent performer, prendre du plaisir et atteindre leurs objectifs
        sportifs.</p>
    <p>En effet, nous sommes convaincus qu’une activité sportive et la sociabilisation que celle-ci engrange, est bénéfique
        pour les personnes porteuses de handicap, encore trop souvent écartées de la société.</p>
    <p><img src="" alt="text description" id="img"></p>

    <h2>2) Mobilité</h2>

    <p>Intervenir dans les frais de déplacement des personnes déficientes visuelles actives qui doivent effectuer les
        trajets de leur domicile au travail (et inversement), de leur domicile au lieu d’entrainement (et inversement)
        lorsque les transports en communs sont inexistants ou inadaptés. En effet, la locomotion vers ces deux pôles est
        encore trop souvent un obstacle majeur.
        Ces deux secteurs, le travail et le sport, sont pourtant primordiaux dans la socialisation de tout un chacun.</p>
