    <h1 class="text-center">Notre histoire :</h1>
    <p>Créée en septembre 2020, l’association sans but lucratif « VOY » a été fondée dans le but de faciliter l’accès au
        sport et au travail aux personnes déficientes visuelles.</p>
    <p>Lors de sa carrière sportive internationale et professionnelle, Dortu David, déficient visuel à l’initiative du
        projet, n’a que trop souvent buter sur des difficultés de mobilité.</p>
    <p>En effet, pour se rendre de bonne heure au travail ou rentrer tard de l’entrainement, les possibilités des transports
        en commun sont réduites voire nulles dans certaines parties du pays.</p>
    <p>C’est pour cette raison que David s’est entouré de personnes de qualité afin de créer une asbl ainsi qu’un club de
        cécifoot en province de Liège où rien n’existait encore au niveau du handisport pour déficients visuels. Dès lors,
        Nulens Cédric, Nulens Yves et Helman Jérémy, respectivement éducateur spécialisé, membre de la cellule « scooting »
        du Standard de Liège et assistant clientèle principal à la SNCB (aide aux PMR), ont rejoint le projet.</p>
    <p>C’est dans cette synergie qu’une collaboration étroite avec le Standard de Liège a pu voir le jour et voir naitre
        l’équipe de Cécifoot B1 (football adapté aux personnes mal – et non-voyantes).</p>
@endsection
